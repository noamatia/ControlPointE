def obj_centered_camera_pos(dist, phi_deg, theta_deg):
    phi = float(phi_deg) / 180 * math.pi
    theta = float(theta_deg) / 180 * math.pi
    x = (dist * math.cos(theta) * math.cos(phi))
    y = (dist * math.sin(theta) * math.cos(phi))
    z = (dist * math.sin(phi))
    return x, y, z
